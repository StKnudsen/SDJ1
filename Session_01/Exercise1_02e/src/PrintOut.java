public class PrintOut
{
  public static void main(String[] args)
  {
    int apples;
    double price, total;

    apples = 5;
    price = 2.25;
    total = apples * price;

    System.out.println("I have " + apples + " apples");
    System.out.println("The price per apple is " + price);
    System.out.println("The total is " + total);
  }
}
